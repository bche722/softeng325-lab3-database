DROP TABLE IF EXISTS CONCERT;
DROP TABLE IF EXISTS PERFORMER;

CREATE TABLE CONCERT(
ID              LONG PRIMARY KEY,
TITLE           VARCHAR(255),
DATE            TIMESTAMP,
FK_PERFORMER_ID LONG);

CREATE TABLE PERFORMER(
ID      LONG PRIMARY KEY,
NAME    VARCHAR(255),
S3IMAGE VARCHAR(255),
GENRE   VARCHAR(30));

ALTER TABLE CONCERT ADD FOREIGN KEY (FK_PERFORMER_ID) REFERENCES PERFORMER(ID);

